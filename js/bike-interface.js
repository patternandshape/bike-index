var displayMissingBike = require ('../js/bike.js').displayMissingBike;

$(document).ready(function() {
  $('#missingBike').click(function() {
    //get city from user
    var city = $('#yourCity').val();
    //clear out city field
    $('#yourCity').val("");
    //run api call
    displayMissingBike(city);
    event.preventDefault();
});
});
